	protected void _refresh() {
		
	}
	
	public void delay( int n ) {
		if (DataLayerView.stop) {
			return;
		}
		
		_refresh();

		try {
			Thread.sleep(n);
		} catch (InterruptedException e) {
		}
	}

	public boolean slowWhile() {

		if (DataLayerView.stop) {
			return false;
		}
		
		try {
			if ( System.currentTimeMillis() - lastRun < 100 ) {
				Thread.sleep(100);
				lastRun = System.currentTimeMillis();
				System.out.println("autodelayed");
			}
		} catch (InterruptedException e) {
		}
		
		if ( DataLayerView.stop || --whileCounter <= 0 ) {
			stopScript();
			return false;
		}
		
		return true;
	}
	
	public void stopScript() {
		if (display.isDisposed()) {
			return;
		}
		display.asyncExec(new Runnable() {
			public void run() {
				if (display.isDisposed()) {
					return;
				}
				
				CodeEditView editorView = null;
				try {
					IViewReference[] refs = PlatformUI.getWorkbench().getActiveWorkbenchWindow().getActivePage().getViewReferences();
					for ( int i = 0; i < refs.length; i++ ) {
						if ( DataLayerView.ORG_ZEFER_LCDPAINTER_CODEVIEW.equals(refs[i].getId()) ) {
							editorView = (CodeEditView)refs[i].getView(true);
							editorView.btnRun.setEnabled(true);
							editorView.btnStop.setEnabled(false);
						}
					}
				} catch (Exception e) {
				}
				
//				DataLayerView.setDisplayImage(null);
				DataLayerView.stop = true;
				DataLayerView.instance = null;

				IWorkbenchWindow window = PlatformUI.getWorkbench().getActiveWorkbenchWindow();
				ICommandService commandService = (ICommandService) window.getService(ICommandService.class);
				if (commandService != null) {
					commandService.refreshElements(RunCommand.ID, null);
					commandService.refreshElements(TerminateCommand.ID, null);
				}
			}
		});
	}
	
